package com.miles.dp.entity;

public class IndexDes {
	private String indexId;
	private String indexType;
	private String indexName;
	private String formula;
	private String indicators;
	private String indexOrder;
	private String isCity;
	private String calLvl;
	
	public String getIndexId() {
		return indexId;
	}
	public void setIndexId(String indexId) {
		this.indexId = indexId;
	}
	public String getIndexType() {
		return indexType;
	}
	public void setIndexType(String indexType) {
		this.indexType = indexType;
	}
	public String getIndexName() {
		return indexName;
	}
	public void setIndexName(String indexName) {
		this.indexName = indexName;
	}
	public String getFormula() {
		return formula;
	}
	public void setFormula(String formula) {
		this.formula = formula;
	}
	public String getIndicators() {
		return indicators;
	}
	public void setIndicators(String indicators) {
		this.indicators = indicators;
	}
	public String getIndexOrder() {
		return indexOrder;
	}
	public void setIndexOrder(String indexOrder) {
		this.indexOrder = indexOrder;
	}
	public String getIsCity() {
		return isCity;
	}
	public void setIsCity(String isCity) {
		this.isCity = isCity;
	}
	public String getCalLvl() {
		return calLvl;
	}
	public void setCalLvl(String calLvl) {
		this.calLvl = calLvl;
	}

}
